from rest_framework import serializers
from .models import AuditLog

class AuditLogSerializer(serializers.ModelSerializer):
    actor_username = serializers.CharField(source="actor.username", read_only=True)

    class Meta:
        model = AuditLog
        fields = [
            "id",
            "actor",
            "actor_username",
            "actor_role",
            "action",
            "entity_type",
            "entity_id",
            "before",
            "after",
            "ip_address",
            "endpoint",
            "created_at",
        ]
